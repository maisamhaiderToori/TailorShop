package com.junia.tailorshop.enums;

/**
 * The SizeEnum enumeration defines the various size options 
 * available for clothing in the tailor shop system.
 * The available sizes are:
 * - S: Small
 * - M: Medium
 * - L: Large
 * - XL: Extra Large
 * - XXL: Double Extra Large
 * - XXXL: Triple Extra Large
 */
public enum SizeEnum {
    S, M, L, XL, XXL, XXXL;
}
